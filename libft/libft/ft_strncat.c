#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		a;

	i = 0;
	a = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[a] != '\0' && n-- > 0)
		s1[i++] = s2[a++];
	s1[i] = '\0';
	return (s1);
}
