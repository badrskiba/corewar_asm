#include "libft.h"

/*
**************************************************
**												**
**	char_to_str :								**
**	Convert a wchar into a wstring 				**
**												**
**************************************************
*/

int		*wchar_to_wstr(int c)
{
	int		*s;

	if (!(s = (int*)malloc(sizeof(char) * 2)))
		return (NULL);
	s[0] = c;
	s[1] = '\0';
	return (s);
}
