/*
**************************************************
**												**
**	char_to_str :								**
**	Convert a char into a string 				**
**												**
**************************************************
*/

char		*char_to_str(int c)
{
	char *s;

	if (!(s = (char*)malloc(sizeof(char) * 2)))
		return (NULL);
	s[0] = c;
	s[1] = '\0';
	return (s);
}
