#include "libft.h"

/*
******************************************************
**													**
**	hide_char()										**
**	hide the char number i from the string s 		**
**													**
******************************************************
*/

char	*hide_char(char *s, int i)
{
	return (ft_strjoin(before_stri(s, i), s + i + 1));
}
